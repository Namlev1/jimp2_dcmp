public class Symbol {
    private final char sybmol;
    private final int length;

    public Symbol(char sybmol, int length) {
        this.sybmol = sybmol;
        this.length = length;
    }

    public char getSybmol() {
        return sybmol;
    }

    public int getLength() {
        return length;
    }

}
